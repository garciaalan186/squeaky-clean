"""Inject a discover_implementation() shim into a Squeaky-generated project.

This is documented methodology overhead: Squeaky-as-shipped does not
honor the `discover_implementation()` contract specified in the
ProblemSpec description, so to score Squeaky on the same neutral test
surface as vanilla Opus, we introspect the generated project's source
tree and write a tests/conftest.py that exposes the contract.

The shim:
- Walks src/ to discover top-level classes
- Identifies IngestedEvent, IngestEventUseCase, PublishEventUseCase by name
- Wraps the use-case classes' methods as plain callables
- Provides fake/unreachable producer factories
- Falls back gracefully when a component is missing

Apply via inject_shim(project_dir).
"""
from __future__ import annotations

import ast
from dataclasses import dataclass
from pathlib import Path


@dataclass(frozen=True)
class _ClassRef:
    """Discovered class: dotted import path + name."""

    import_path: str
    class_name: str


def inject_shim(project_dir: Path) -> bool:
    """Write a discover_implementation() conftest.py shim. Returns success."""
    src = project_dir / "src"
    if not src.exists():
        return False
    classes = _discover_classes(src)
    shim = _build_shim(classes)
    tests_dir = project_dir / "tests"
    tests_dir.mkdir(exist_ok=True)
    (tests_dir / "conftest.py").write_text(shim)
    return True


def _discover_classes(src_dir: Path) -> dict[str, _ClassRef]:
    """Map class-name → ClassRef for every class declared under src/."""
    found: dict[str, _ClassRef] = {}
    for py_file in src_dir.rglob("*.py"):
        if py_file.name == "__init__.py":
            continue
        try:
            tree = ast.parse(py_file.read_text())
        except (OSError, SyntaxError):
            continue
        rel = py_file.relative_to(src_dir.parent)
        dotted = ".".join(rel.with_suffix("").parts)
        for node in tree.body:
            if isinstance(node, ast.ClassDef):
                if node.name not in found:
                    found[node.name] = _ClassRef(import_path=dotted, class_name=node.name)
    return found


def _build_shim(classes: dict[str, _ClassRef]) -> str:
    parts: list[str] = [_HEADER]
    parts.append(_imports_block(classes))
    parts.append(_factories_block())
    parts.append(_discover_function(classes))
    return "\n\n".join(parts) + "\n"


def _imports_block(classes: dict[str, _ClassRef]) -> str:
    """Emit `from <path> import <Class>` lines for the names we'll use.

    We try the following names; missing ones become None placeholders so
    discover_implementation() can return a partial dict.
    """
    wanted = ("IngestedEvent", "IngestEventUseCase", "PublishEventUseCase")
    lines: list[str] = []
    for name in wanted:
        ref = classes.get(name)
        if ref is None:
            lines.append(f"{name} = None  # not found in project")
        else:
            lines.append(f"try:\n    from {ref.import_path} import {ref.class_name}\nexcept ImportError:\n    {ref.class_name} = None")
    return "\n".join(lines)


def _factories_block() -> str:
    return _FACTORIES


def _discover_function(classes: dict[str, _ClassRef]) -> str:
    return _DISCOVER


